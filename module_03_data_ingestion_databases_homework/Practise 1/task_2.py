"""
Расчет длины ограждения для квадратной зоны хранения.

Департамент логистики запрашивает инструмент для быстрого расчета 
геометрических параметров новых квадратных зон хранения. 
Требуется скрипт, который принимает идентификатор зоны и рассчитывает 
длину её ограждения на основе известной площади.

Функция рассчитывает периметр квадратной зоны по формуле:
P = 4 * sqrt(S), где S - площадь зоны.

Args:
    zone_id (int): Уникальный идентификатор зоны хранения.
    area (float): Площадь зоны в квадратных метрах (м²).

Returns:
    float: Длина ограждения (периметр) в метрах (м).

Raises:
    ValueError: Если area <= 0, так как площадь должна быть положительным числом.
    TypeError: Если zone_id не является целым числом или area не является числом.

Example:
    >>> calculate_fence_length(101, 256)
    64.0
    
    >>> calculate_fence_length(205, 100.5)
    40.1

Note:
    Результат округляется до 2 знаков после запятой для практического использования.
    Зона считается идеально квадратной для упрощения расчетов.
"""

import math

area_total = 225
cold_storage_01 = input('Введите идентификатор зоны: Cold-Storage-01: ')
print(f'Расчет для объекта: {cold_storage_01}')
print(f'Длина стены зоны: {math.sqrt(area_total)} метров')